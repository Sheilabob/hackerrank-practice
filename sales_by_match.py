def sockMerchant(n, ar):
    # Write your code here
    holder = []
    pair_count = 0
    for i in range(n):
        if ar[i] not in holder:
            new_pairs = (ar.count(ar[i])//2)
            pair_count += new_pairs
        holder.append(ar[i])
    return pair_count

